using namespace std;

int main() {

    int struja;
    cin >> struja;

    float cena = 0;
    if (struja <= 500) {
        cena = struja * 5;
    } else if (struja <= 650) {
        cena = (struja - 500) * 7.5 + 5 * 500;
    } else if (struja <= 850) {
        cena = (struja - 650) * 11 + 150 * 7.5 + 5 * 500;
    } else {
        cena = (struja - 850) * 13.5 + 150 * 7.5 + 5 * 500 + 200 * 11;
    }

    if (cena <= 7000) {
        cout << cena * 1.1;
    } else {
        cout << cena * 1.18;
    }

    return 0;
}